##信息流程插件定义
CREATE TABLE WF_INFO_PROC_PLUGIN_DEF
( 
	PLUGIN_NAME VARCHAR (100)  NOT NULL ,
	PLUGIN_TYPE VARCHAR (32)  NOT NULL ,
	PLUGIN_PATH  VARCHAR (200)  NOT NULL , 
	XML_PARSER VARCHAR (200) ,
	PARSER_TYPE CHAR (1) DEFAULT '0',
	ITEM_TYPE CHAR (1) DEFAULT '0' ,
	JS_PATH VARCHAR (200) ,
	JS_OBJECT VARCHAR (32) ,
	FILE_PARSER VARCHAR (200) 
