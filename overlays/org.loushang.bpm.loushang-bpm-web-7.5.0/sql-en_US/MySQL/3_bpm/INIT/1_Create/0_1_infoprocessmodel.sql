##信息流程模型定义
CREATE TABLE WF_INFO_PROCESS_DEF_MODEL
(
    ID VARCHAR(32) NOT NULL,
    PROC_DEF_ID VARCHAR (32) NOT NULL,
    PROC_DEF_NAME VARCHAR (100),
    PLUGIN_TYPE VARCHAR (32) NOT NULL,
    CREATE_TIME CHAR (17) NOT NULL,
    PROC_SPEC VARCHAR(32),
    MODEL_CONTENT BLOB NOT NULL ,
    EXTENSION_CONTENT BLOB ,
    IS_UTF8 CHAR (1),
    IS_HISTORY CHAR (1),
    PROCESS_TYPE VARCHAR(32),
    IS_FREEDOM CHAR(1) DEFAULT '0'
);
##主键
ALTER TABLE WF_INFO_PROCESS_DEF_MODEL  
  ADD CONSTRAINT  PRODEFMODPK  
  PRIMARY KEY (ID);

